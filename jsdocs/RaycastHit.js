class RaycastHit {


    /**
     * The barycentric coordinate of the triangle we hit.
     */
    get barycentricCoordinate() {}

    /**
     * The barycentric coordinate of the triangle we hit.
     */
    set barycentricCoordinate(value) {}

    /**
     * The Collider that was hit.
     */
    get collider() {}

    /**
     * The Collider that was hit.
     */
    set collider(value) {}

    /**
     * The distance from the ray&#039;s origin to the impact point.
     */
    get distance() {}

    /**
     * The distance from the ray&#039;s origin to the impact point.
     */
    set distance(value) {}

    /**
     * The uv lightmap coordinate at the impact point.
     */
    get lightmapCoord() {}

    /**
     * The uv lightmap coordinate at the impact point.
     */
    set lightmapCoord(value) {}

    /**
     * The normal of the surface the ray hit.
     */
    get normal() {}

    /**
     * The normal of the surface the ray hit.
     */
    set normal(value) {}

    /**
     * The impact point in world space where the ray hit the collider.
     */
    get point() {}

    /**
     * The impact point in world space where the ray hit the collider.
     */
    set point(value) {}

    /**
     * The Rigidbody of the collider that was hit. If the collider is not attached to a rigidbody then it is null.
     */
    get rigidbody() {}

    /**
     * The Rigidbody of the collider that was hit. If the collider is not attached to a rigidbody then it is null.
     */
    set rigidbody(value) {}

    /**
     * The uv texture coordinate at the impact point.
     */
    get textureCoord() {}

    /**
     * The uv texture coordinate at the impact point.
     */
    set textureCoord(value) {}

    /**
     * The secondary uv texture coordinate at the impact point.
     */
    get textureCoord2() {}

    /**
     * The secondary uv texture coordinate at the impact point.
     */
    set textureCoord2(value) {}

    /**
     * The Transform of the rigidbody or collider that was hit.
     */
    get transform() {}

    /**
     * The Transform of the rigidbody or collider that was hit.
     */
    set transform(value) {}

    /**
     * The index of the triangle that was hit.
     */
    get triangleIndex() {}

    /**
     * The index of the triangle that was hit.
     */
    set triangleIndex(value) {}


}