class WebGLExceptionSupport {


    /**
     * Disable exception support.
     */
    get None() {}

    /**
     * Disable exception support.
     */
    set None(value) {}

    /**
     * Enable throw support.
     */
    get ExplicitlyThrownExceptionsOnly() {}

    /**
     * Enable throw support.
     */
    set ExplicitlyThrownExceptionsOnly(value) {}

    /**
     * Enable exception support for all exceptions.
     */
    get Full() {}

    /**
     * Enable exception support for all exceptions.
     */
    set Full(value) {}


}