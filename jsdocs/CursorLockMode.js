class CursorLockMode {


    /**
     * Cursor behavior is unmodified.
     */
    get None() {}

    /**
     * Cursor behavior is unmodified.
     */
    set None(value) {}

    /**
     * Lock cursor to the center of the game window.
     */
    get Locked() {}

    /**
     * Lock cursor to the center of the game window.
     */
    set Locked(value) {}

    /**
     * Confine cursor to the game window.
     */
    get Confined() {}

    /**
     * Confine cursor to the game window.
     */
    set Confined(value) {}


}